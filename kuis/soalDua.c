#include <stdio.h>
int main(void) {
    // int x = 112853627 % 2; 
    // x ^= 4 * x;

    int x = 42, y=19;
    x |= y * 14 & 8;
    printf("%d\n", x);
    return 0;
}
    
// int x = 112853627 % 2 -> 1 
// 0001
// 4 * x -> 4 * 1 = 4
// 0100
// x ^= 4 * x -> 1 ^ 4 = 5
// 0001
// 0100
// -----
// 0101 -> 5


// 42 = 0010 1010
// 19 = 0001 0011
// 14 = 0000 1110
// 8  = 0000 1000

// y * 14 = 19 * 14 = 266
// 266 = 0001 0000 1010

// y * 14 & 8
// 0001 0000 1010
// 0000 0000 1000
// -----------------
// 0000 0000 1000 -> 8

// x |= y * 14 & 8
// 0010 1010
// 0000 1000
// -----------------
// 0010 1010 -> 42  